#include<stdio.h>

void DisplayASCII()
{
    int i =0;
    printf("-----------------------------------------------------------------------------------------------------------\n");
    printf("---------------------------------------------ASCII TABLE--------------------------------------------------------------\n");
    printf("----------------------------------------------------------------------------------------------------------------\n");
    printf("\t\tCharacter\tDecimal\t\tHexa\tOctal\n");
    for(i=0;i<255;i++)
    {
        printf("\t\t%c\t\t%d\t\t%x\t\t%o\n",i,i,i,i);
    }
    printf("--------------------------------------------------------------------------------------------------------------------\n");
}
int main()
{
    DisplayASCII();

    return 0;
}